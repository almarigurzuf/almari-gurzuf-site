export function initDetails() {
    const modal = document.getElementById('detailsModal');
    if (!modal) return;
    const closeBtn = modal.querySelector('.details-modal-close');
    const detailButtons = document.querySelectorAll('.btn-details');

    let currentSlide = 0;
    let totalSlides = 0;

    detailButtons.forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.preventDefault();
            const id = btn.getAttribute('data-apt-id');
            const data = apartmentsData[id];

            if (data) {
                fillModal(data, id);
                modal.classList.add('active');
                document.body.style.overflow = 'hidden';
                currentSlide = 0;
                updateSliderPosition();
            }
        });
    });

    function fillModal(data, id) {
        // Initialize Slider Content
        const track = document.getElementById('modalSliderTrack');
        const dotsContainer = document.getElementById('modalSliderDots');
        track.innerHTML = '';
        dotsContainer.innerHTML = '';

        const images = data.gallery || [data.image];
        totalSlides = images.length;

        images.forEach((imgSrc, index) => {
            const imgContainer = document.createElement('div');
            imgContainer.style.minWidth = '100%';
            imgContainer.style.height = '100%';

            const img = document.createElement('img');
            img.src = imgSrc;
            img.alt = `${data.title} - photo ${index + 1}`;
            img.style.cssText = 'width:100%; height:100%; object-fit:cover; cursor:pointer;';
            imgContainer.appendChild(img);

            imgContainer.querySelector('img').onclick = () => {
                const lightbox = document.getElementById('lightbox');
                const lightboxImg = document.getElementById('lightboxImg');
                if (lightbox && lightboxImg) {
                    lightboxImg.src = imgSrc;
                    lightbox.classList.add('active');
                }
            };
            track.appendChild(imgContainer);

            const dot = document.createElement('div');
            dot.className = 'modal-dot' + (index === 0 ? ' active' : '');
            dot.onclick = () => {
                currentSlide = index;
                updateSliderPosition();
            };
            dotsContainer.appendChild(dot);
        });

        document.getElementById('modalTitle').textContent = data.title;
        document.getElementById('modalDescription').textContent = data.description;
        document.getElementById('modalArea').textContent = data.area;
        document.getElementById('modalCapacity').textContent = data.capacity;
        document.getElementById('modalRooms').textContent = data.rooms;

        const prevBtn = modal.querySelector('.modal-slider-btn.prev');
        const nextBtn = modal.querySelector('.modal-slider-btn.next');

        prevBtn.onclick = (e) => {
            e.stopPropagation();
            currentSlide = (currentSlide - 1 + totalSlides) % totalSlides;
            updateSliderPosition();
        };

        nextBtn.onclick = (e) => {
            e.stopPropagation();
            currentSlide = (currentSlide + 1) % totalSlides;
            updateSliderPosition();
        };

        const amenitiesContainer = document.getElementById('modalAmenities');
        amenitiesContainer.innerHTML = '';
        data.amenities.forEach(item => {
            const div = document.createElement('div');
            div.className = 'amenity-detail';
            const icon = document.createElement('i');
            icon.className = `fas ${item.icon}`;
            const span = document.createElement('span');
            span.textContent = item.text;
            div.appendChild(icon);
            div.appendChild(span);
            amenitiesContainer.appendChild(div);
        });

        const bookBtn = document.getElementById('modalBookBtn');
        bookBtn.onclick = () => {
            modal.classList.remove('active');
            document.body.style.overflow = '';

            // Прямая ссылка на бронирование конкретного номера
            const checkin = document.getElementById('checkin')?.value || '';
            const checkout = document.getElementById('checkout')?.value || '';
            const adults = document.getElementById('adults')?.value || '2';

            const bnovoUrl = `/booking.html?arrival=${checkin}&departure=${checkout}&adults=${adults}&room_type_id=${data.bnovoId}`;

            window.open(bnovoUrl, '_blank');
        };
    }

    function updateSliderPosition() {
        const track = document.getElementById('modalSliderTrack');
        const dots = document.querySelectorAll('.modal-dot');
        if (track) track.style.transform = `translateX(-${currentSlide * 100}%)`;
        dots.forEach((dot, index) => dot.classList.toggle('active', index === currentSlide));
    }

    closeBtn.addEventListener('click', () => {
        modal.classList.remove('active');
        document.body.style.overflow = '';
    });

    window.addEventListener('click', (e) => {
        if (e.target === modal) {
            modal.classList.remove('active');
            document.body.style.overflow = '';
        }
    });

    // Touch support for modal slider
    const track = document.getElementById('modalSliderTrack');
    let touchStartX = 0;
    let touchEndX = 0;

    track.addEventListener('touchstart', (e) => {
        touchStartX = e.changedTouches[0].screenX;
    }, { passive: true });

    track.addEventListener('touchend', (e) => {
        touchEndX = e.changedTouches[0].screenX;
        handleModalSwipe();
    }, { passive: true });

    function handleModalSwipe() {
        const swipeThreshold = 50;
        if (touchEndX < touchStartX - swipeThreshold) {
            // Swipe Left -> Next
            currentSlide = (currentSlide + 1) % totalSlides;
            updateSliderPosition();
        } else if (touchEndX > touchStartX + swipeThreshold) {
            // Swipe Right -> Prev
            currentSlide = (currentSlide - 1 + totalSlides) % totalSlides;
            updateSliderPosition();
        }
    }
}
